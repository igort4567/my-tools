export interface Tool {
  /** Уникальный слаг — используется в URL: /tools/{slug} и должен совпадать
   *  с именем папки внутри app/tools/ */
  slug: string;
  /** Каталожный номер инструмента, например "T-01". Придумывается один раз
   *  и не меняется — на него могут ссылаться извне. */
  code: string;
  /** Название инструмента, отображается в карточке и на странице */
  title: string;
  /** Короткое описание для карточки в каталоге (1 предложение) */
  description: string;
  /** Название категории — должно совпадать с полем title в Category */
  category: string;
}

export interface Category {
  /** Слаг категории — используется как id секции: /tools#{slug} */
  slug: string;
  /** Название категории, отображается на карточке и как заголовок секции */
  title: string;
  /** Короткое описание категории для главной страницы */
  description: string;
}
